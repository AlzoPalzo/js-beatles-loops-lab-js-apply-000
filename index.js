function theBeatlesPlay(musicians, instruments)
{
  var beatlesArray = {}
  for(let i = 0; i < musicians.length + 1; i ++)
  {
    var beatleString = musicians[i] + " plays " + instruments[i]
    beatlesArray = [...beatlesArray, beatleString]
  }
  return beatlesArray
}

